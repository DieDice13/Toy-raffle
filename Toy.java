// Класс Toy представляет игрушку
public class Toy {
    String id; // уникальный идентификатор игрушки
    String name; // имя игрушки
    int weight; // вес игрушки
    int quantity; // количество игрушек

    // Конструктор класса Toy
    public Toy(String id, String name, int weight, int quantity) {
        this.id = id; // инициализация идентификатора
        this.name = name; // инициализация имени
        this.weight = weight; // инициализация веса
        this.quantity = quantity; // инициализация количества
    }
}


